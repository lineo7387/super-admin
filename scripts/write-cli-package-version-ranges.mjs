#!/usr/bin/env node
import { writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { getSuperAdminDependencyVersionRanges } from './release-plan.mjs'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const outputPath = resolve(repoRoot, 'packages/cli/src/package-version-ranges.generated.ts')

function formatVersionRanges(versionRanges) {
  const entries = Object.entries(versionRanges)
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([packageName, versionRange]) => `  '${packageName}': '${versionRange}'`)
    .join(',\n')

  return `// Generated by scripts/write-cli-package-version-ranges.mjs. Do not edit by hand.

export const superAdminPackageVersionRanges = {
${entries}
} as const
`
}

await writeFile(outputPath, formatVersionRanges(getSuperAdminDependencyVersionRanges()))
